	// Validate form
	function ValidateForm() {
		global $Language, $gsFormError;

		// Initialize form error message
		$gsFormError = "";

		// Check if validation required
		if (!EW_SERVER_VALIDATE)
			return ($gsFormError == "");
		if (!$this->reg_id->FldIsDetailKey && !is_null($this->reg_id->FormValue) && $this->reg_id->FormValue == "") {
			ew_AddMessage($gsFormError, str_replace("%s", $this->reg_id->FldCaption(), $this->reg_id->ReqErrMsg));
		}
		if (!ew_CheckInteger($this->reg_id->FormValue)) {
			ew_AddMessage($gsFormError, $this->reg_id->FldErrMsg());
		}
		if (!$this->_email->FldIsDetailKey && !is_null($this->_email->FormValue) && $this->_email->FormValue == "") {
			ew_AddMessage($gsFormError, str_replace("%s", $this->_email->FldCaption(), $this->_email->ReqErrMsg));
